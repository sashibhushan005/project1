def for3():
    print("(a+b)^3 = (a)^3+(b)^3+[3*(a)^2*b]+[3*a*(b)^2]")
    c0 = (a * a * a)
    print("a*a*a =", c0)
    c1 = (b * b * b)
    print("b*b*b =", c1)
    c2 = (3 * (a * a) * b)
    print("3*(a*a)*b =", c2)
    c3 = (3 * (b * b) * a)
