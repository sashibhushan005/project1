def for1():
    print("(a+b)^2 = (a * a) + (b * b) + (2 * a * b)")
    c0 = (a * a)
    print("a*a =", c0)
    c1 = (b * b)
    print("b*b =", c1)
    c2 = (2 * a * b)
    print("2*a*b =", c2)
    x = c0 + c1 + c2
    print(c0, "+", c1, "+", c2, "=", x)
    print("total =", x)
